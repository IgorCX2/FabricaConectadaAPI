    qtd: {
        type: DataTypes.STRING,
        allowNull: true,
    },
    supLevel2Code: {
        type: DataTypes.STRING,
        allowNull: true,
    },
    turno: {
        type: DataTypes.STRING,
        allowNull: true,
    },
    statusPCF: {
        type: DataTypes.STRING,
        allowNull: true,
    },
    comprovante: {
        type: DataTypes.STRING,
        allowNull: true,
    },
